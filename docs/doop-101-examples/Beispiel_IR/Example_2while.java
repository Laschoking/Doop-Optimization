import java.io.*;

public class Example_2while
{
    public static void main(final String[] args) throws IOException {
		
    }
	public void while_loop(){
		int i = 0;
	
		while (i < 10){
			i += 1;
			System.out.println("hallo");
		}
	}
	public void for_loop(){
		for (int i = 0; i < 10; i += 1){
		System.out.println("hallo");
		}  
}  
}
    

